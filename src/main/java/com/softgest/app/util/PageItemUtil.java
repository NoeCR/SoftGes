package com.softgest.app.util;

public class PageItemUtil {

	
	private int numero;
	private boolean actual;
	public PageItemUtil(int numero, boolean actual) {
		this.numero = numero;
		this.actual = actual;
	}
	public int getNumero() {
		return numero;
	}
	public boolean isActual() {
		return actual;
	}
	
	
	
}
